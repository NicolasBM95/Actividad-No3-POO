    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double ReteFuente, SalarioBruto, SalarioNeto, HorasMes, ValorHora;
        int codigo_empleado;
        String nombre;
        
        txtHorasTrabajoMes.getText();
        txtValorHoras.getText();
        txtRetefuente.getText();
        
        HorasMes = Double.parseDouble(txtHorasTrabajoMes.getText());
        ValorHora = Double.parseDouble(txtValorHoras.getText());
        ReteFuente = Double.parseDouble(txtRetefuente.getText());
        
        SalarioBruto = ValorHora*HorasMes;
        SalarioNeto = SalarioBruto-(SalarioBruto*(ReteFuente/100));
        
        txtSalarioBruto.setText(String.valueOf(SalarioBruto));
        txtSalarioNeto.setText(String.valueOf(SalarioNeto));
    }//GEN-LAST:event_btnCalcularActionPerformed

